#include<iostream>
using namespace std;
int main()
{
    int myInt = 5;
    char myChar = 'a';
    bool myBool = 'True';
    cout<<"The size of the Int datatype is "<<sizeof(myInt)<<endl;
    cout<<"The size of the Char datatype is "<<sizeof(myChar)<<endl;
    cout<<"The size of the Bool datatype is "<<sizeof(myBool)<<endl;
}
